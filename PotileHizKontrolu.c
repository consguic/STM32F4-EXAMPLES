float adcValue= 0;
float newValue= 0;

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_ADC1_Init(void);
static void MX_TIM1_Init(uint16_t timPulse);

ADC_HandleTypeDef hadc1;
TIM_HandleTypeDef htim1;

float adcRead(){
	HAL_ADC_Start(&hadc1);
	if (HAL_ADC_PollForConversion(&hadc1, 500) == HAL_OK){
		adcValue = HAL_ADC_GetValue(&hadc1);
	}
	return adcValue;
}

float map(float adcValue, float max, float min, float conMax, float conMin){
	  return adcValue * ((conMax-conMin)/(max-min));
}

int main(void){
  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_ADC1_Init();

  while (1)
  {
    adcValue = adcRead();
    newValue = map(adcValue,4095,0,2399,0);
    MX_TIM1_Init(newValue);
  }
}

